# 6. MODEL 4 — Gaussian Process Regression (Kriging)
#    Hyperparameters: kernel length-scale bounds, noise level
#    Strategy: multiple kernel restarts within each outer fold
#    Note: GPR optimises its own kernel hyperparameters via marginal likelihood
#    maximisation — no additional grid search needed. We tune n_restarts to
#    control how thoroughly the kernel optimiser searches.
#    Grid: n_restarts ∈ {3, 7, 15}
# ─────────────────────────────────────────────────────────────────────────────
print("\n── Model 4: Gaussian Process (Kriging) ──────────────────")
print("  GPR self-optimises kernel hyperparameters via marginal likelihood.")
print("  Grid: n_restarts_optimizer ∈ {3, 7, 15}")
t0 = time.time()

GPR_RESTARTS = [3, 7, 15]

def build_gpr_nested(Xtr, ytr):
    """
    Selects n_restarts_optimizer via inner CV, then returns the best GPR.
    GPR does not wrap into GridSearchCV easily; we manually loop.
    """
    inner_cv = KFold(n_splits=INNER_FOLDS, shuffle=True, random_state=42)
    sc       = StandardScaler()
    Xsc      = sc.fit_transform(Xtr)

    best_restarts = GPR_RESTARTS[0]
    best_rmse     = np.inf

    for n_rest in GPR_RESTARTS:
        kernel = (C(1.0, (1e-3, 1e3))
                  * RBF(length_scale=[1.0]*4, length_scale_bounds=(1e-2, 1e2))
                  + WhiteKernel(noise_level=1e-2, noise_level_bounds=(1e-5, 1e1)))
        fold_rmses = []
        for tr_idx, vl_idx in inner_cv.split(Xsc):
            gpr = GaussianProcessRegressor(
                kernel=kernel, n_restarts_optimizer=n_rest,
                normalize_y=True, random_state=42)
            gpr.fit(Xsc[tr_idx], ytr[tr_idx])
            pv = gpr.predict(Xsc[vl_idx])
            fold_rmses.append(np.sqrt(mean_squared_error(ytr[vl_idx], pv)))
        cv_rmse = np.mean(fold_rmses)
        if cv_rmse < best_rmse:
            best_rmse     = cv_rmse
            best_restarts = n_rest

    # Refit on full training set with best n_restarts
    kernel_final = (C(1.0, (1e-3, 1e3))
                    * RBF(length_scale=[1.0]*4, length_scale_bounds=(1e-2, 1e2))
                    + WhiteKernel(noise_level=1e-2, noise_level_bounds=(1e-5, 1e1)))
    gpr_final = GaussianProcessRegressor(
        kernel=kernel_final, n_restarts_optimizer=best_restarts,
        normalize_y=True, random_state=42)
    gpr_final.fit(Xsc, ytr)

    class Wrap:
        def predict(self, Xt):
            return gpr_final.predict(sc.transform(Xt))
        best_params_ = {"n_restarts_optimizer": best_restarts}
    return Wrap()

gpr_preds = nested_loocv(build_gpr_nested, X, y, "GPR")
