def compute_metrics(y_true, y_pred, name=""):
    rmse = np.sqrt(mean_squared_error(y_true, y_pred))
    mae  = mean_absolute_error(y_true, y_pred)
    r2   = r2_score(y_true, y_pred)
    mape = np.mean(np.abs((y_true - y_pred) / y_true)) * 100
    return {"Model": name, "RMSE": rmse, "MAE": mae, "R²": r2, "MAPE%": mape}


def nested_loocv(build_gs_fn, X, y, model_name):
    """
    Nested LOOCV.
    build_gs_fn(X_train, y_train) → fitted model with .predict()
    Prints selected hyperparameters for each fold when VERBOSE=True.
    """
    loo   = LeaveOneOut()
    preds = np.empty(n)
    best_params_list = []

    for fold, (train_idx, test_idx) in enumerate(loo.split(X)):
        m = build_gs_fn(X[train_idx], y[train_idx])
        preds[test_idx] = m.predict(X[test_idx])

        # Collect best params if model supports it
        if hasattr(m, "best_params_"):
            best_params_list.append(m.best_params_)
        elif hasattr(m, "named_steps"):
            # Pipeline — check last step
            last = list(m.named_steps.values())[-1]
            if hasattr(last, "best_params_"):
                best_params_list.append(last.best_params_)

    if best_params_list and VERBOSE:
        # Summarise most-frequent hyperparameter values across folds
        param_df = pd.DataFrame(best_params_list)
        print(f"\n  {model_name} — hyperparameter selection across {n} folds:")
        for col in param_df.columns:
            counts = param_df[col].value_counts()
            most_common = counts.index[0]
            pct = counts.iloc[0] / n * 100
            print(f"    {col:30s}: most selected = {most_common}  "
                  f"({pct:.0f}% of folds)")

    return preds


