def extract_number(text):
    text = str(text).strip()
    matches = re.findall(r"\d+\.?\d*", text)
    if matches:
        # Último número = resultado final do raciocínio
        val = float(matches[-1])
        return val if 0.01 < val < 50 else np.nan
    return np.nan

