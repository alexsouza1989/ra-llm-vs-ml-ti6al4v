# 5. LOOCV GENÉRICO PARA LLMs
# ─────────────────────────────────────────────────────────────────────────────
def llm_loocv(call_fn, prompt_fn, X, y, label="LLM",
              delay=0.5, verbose=True):
    """
    LOOCV para qualquer LLM.
    call_fn   : função que chama a API (call_claude / call_gpt / call_gemini)
    prompt_fn : função que constrói o prompt (build_fewshot_prompt / build_cot_prompt)
    delay     : segundos entre chamadas (respeita rate limits)
    """
    preds = np.empty(n)
    raw_data = []   # guarda (idx, raw_response, parsed_value)

    for i in range(n):
        # Montar treino (todos exceto i) e teste (ponto i)
        train_idx  = [j for j in range(n) if j != i]
        train_rows = [(X[j,0], X[j,1], X[j,2], X[j,3], y[j])
                      for j in train_idx]
        test_row   = (X[i,0], X[i,1], X[i,2], X[i,3])

        prompt   = prompt_fn(train_rows, test_row)
        raw_resp = call_fn(prompt)
        val      = extract_number(raw_resp)
        preds[i] = val

        raw_data.append({
            "idx":       i,
            "Vc":        X[i,0], "f": X[i,1],
            "ap":        X[i,2], "Re": X[i,3],
            "Ra_true":   y[i],
            "Ra_pred":   val,
            "raw_resp":  raw_resp,
        })

        if verbose:
            status = f"✓ {val:.4f}" if not np.isnan(val) else "✗ parse error"
            print(f"    [{label}] ponto {i+1:2d}/{n} | "
                  f"Ra_true={y[i]:.4f} | Ra_pred={status} | resp='{raw_resp}'")

        time.sleep(delay)

    return preds, pd.DataFrame(raw_data)


