def load_llm_results_and_compare(ml_results_dict):
    """
    Carrega resultados LLM salvos em CSV e monta tabela unificada
    ML + LLM para plotar e reportar na tese.

    ml_results_dict : {nome_modelo: (y_true, y_pred)} — do script ML
    """
    import glob

    llm_rows = []
    for csv_file in glob.glob("raw_responses_*.csv"):
        label = csv_file.replace("raw_responses_", "").replace(".csv", "")
        df_r  = pd.read_csv(csv_file)
        valid = df_r["Ra_pred"].notna() & (df_r["Ra_pred"] > 0)
        if valid.sum() < 5:
            continue
        yt = df_r.loc[valid, "Ra_true"].values
        yp = df_r.loc[valid, "Ra_pred"].values
        llm_rows.append({
            "Model": label,
            "RMSE":  np.sqrt(mean_squared_error(yt, yp)),
            "MAE":   mean_absolute_error(yt, yp),
            "R²":    r2_score(yt, yp),
            "MAPE%": np.mean(np.abs((yt - yp) / yt)) * 100,
            "Tipo":  "LLM",
        })

    ml_rows = []
    for mname, (yt, yp) in ml_results_dict.items():
        valid = ~np.isnan(yp)
        yt_v, yp_v = yt[valid], yp[valid]
        ml_rows.append({
            "Model": mname,
            "RMSE":  np.sqrt(mean_squared_error(yt_v, yp_v)),
            "MAE":   mean_absolute_error(yt_v, yp_v),
            "R²":    r2_score(yt_v, yp_v),
            "MAPE%": np.mean(np.abs((yt_v - yp_v) / yt_v)) * 100,
            "Tipo":  "ML",
        })

    all_rows = ml_rows + llm_rows
    df_all   = (pd.DataFrame(all_rows)
                .set_index("Model")
                .sort_values("RMSE"))
    return df_all


